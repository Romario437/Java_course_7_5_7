package entity;

public class Speed {

    private int s1, s2;


    public int getS1() {
        return s2 - s1;
    }

    public void setS1(int s1) {
        this.s1 = s1;
    }

    public int getS2() {
        return s2 + s1;
    }

    public void setS2(int s2) {
        this.s2 = s2;
    }
}
